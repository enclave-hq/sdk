// ============ Quote & Preview API ============

/**
 * Route and fees query request
 */
export interface RouteAndFeesRequest {
  /** Owner data (source chain and address) */
  ownerData: UniversalAddress;
  /** Deposit token address */
  depositToken: string;
  /** Withdrawal intent */
  intent: {
    type: 'RawToken' | 'AssetToken';
    beneficiary: UniversalAddress;
    tokenSymbol?: string; // For RawToken: token symbol (e.g., "USDT")
    assetId?: string; // For AssetToken: 32-byte Asset ID
    assetTokenSymbol?: string; // For AssetToken: asset token symbol (e.g., "aUSDT")
  };
  /** Amount to withdraw */
  amount: string;
  /** Include Hook execution cost */
  includeHook: boolean;
}

/**
 * Route step information
 */
export interface RouteStep {
  step: number;
  chain: string;
  action: string;
  estimatedTime: string;
}

/**
 * Gas fee information
 */
export interface GasFee {
  chain: string;
  estimatedGas: string;
  currentGasPrice: string;
  gasCostInNative: string;
  gasCostInUSD: string;
  recommendation?: string;
}

/**
 * Bridge fee information
 */
export interface BridgeFee {
  bridgeName: string;
  feeType: string;
  feeInSourceToken: string;
  feeInUSD: string;
  slippage: string;
  minReceived: string;
}

/**
 * Fee summary
 */
export interface FeeSummary {
  totalGasCostUSD: string;
  totalBridgeFeeUSD: string;
  totalCostUSD: string;
  estimatedReceived: string;
}

/**
 * Route and fees response
 */
export interface RouteQuoteResponse {
  route: {
    bridge: string;
    bridgeProtocol: string;
    estimatedTime: string;
    steps: RouteStep[];
  };
  fees: {
    proofGeneration: GasFee;
    executeWithdraw: GasFee;
    bridge: BridgeFee;
    hookExecution?: GasFee;
    summary: FeeSummary;
  };
  timeline: {
    total: string;
    breakdown: Array<{
      stage: string;
      time: string;
    }>;
  };
  warnings: string[];
  meta: {
    quoteId: string;
    validUntil: string;
    timestamp: string;
  };
}

